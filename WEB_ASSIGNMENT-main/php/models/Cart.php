    public function create($data, $allowedKeys = [])
    {
        global $connection;

        if (!empty($allowedKeys)) {
            $data = array_intersect_key($data, array_flip($allowedKeys));
        }

        $keys = array_keys($data);
        $values = array_values($data);
        $query = "INSERT INTO CART (" . implode(", ", $keys) . ") VALUES ('" . implode("', '", $values) . "')";

        try {
            $result = $connection->prepare($query);

            $result->execute();

            return $result;
        } catch (PDOException $e) {
            echo "Unknown error in CART::create: " . $e->getMessage();
        }
    }

    public function update($id, $data, $allowedKeys = [])
    {
        global $connection;

        if (!empty($allowedKeys)) {
            $data = array_intersect_key($data, array_flip($allowedKeys));
        }

        foreach ($data as $key => $value) {
            $updates[] = "$key='$value'";
        }
        if (isset($data['status'])) 
        {
            if($data['status'] == 'ORDERED')
            {
                $updates[] = "ordered_at=CURTIME()";
            }
            else if ($data['status'] == 'DELIVERED')
            {
                $updates[] = "shipped_at=CURTIME()";
            }
        }

        $query = "UPDATE CART SET " . implode(", ", $updates) . " WHERE id='$id'";

        try {
            $result = $connection->prepare($query);

            $result->execute();

            return $result;
        } catch (PDOException $e) {
            echo "Unknown error in CART::update: " . $e->getMessage();
        }
    }

    public function delete($id) {
        global $connection;

        $query = "DELETE FROM CART WHERE id='$id'";

        try {
            $result = $connection->prepare($query);

            $result->execute();

            return $result;
